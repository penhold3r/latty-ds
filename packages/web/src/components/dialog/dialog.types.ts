/**
 * Size variant of the dialog component.
 * Determines the width and overall size of the dialog.
 *
 * - `sm`: 400px max width
 * - `md`: 600px max width (default)
 * - `lg`: 800px max width
 * - `xl`: 1000px max width
 * - `fullscreen`: Full viewport width and height
 */
export type DialogSize = 'sm' | 'md' | 'lg' | 'xl' | 'fullscreen';
